@api_view(['GET', 'POST']) # The @api_view decorator for working with function based views.
def snippet_list(request, format=None):
    """
    List all code snippets, or create a new snippet.
    """
    if request.method == 'GET':
        snippets = Snippet.objects.all()
        serializer = SnippetSerializer(snippets, many=True)
        return Response(serializer.data)

    elif request.method == 'POST':
        serializer = SnippetSerializer(data=request.data)  # request: Handles arbitrary data.  Works for 'POST', 'PUT' and 'PATCH' methods.
        if serializer.is_valid():
            serializer.save()
            return Response(serializer.data, status=status.HTTP_201_CREATED)   # Response: Renders to content type as requested by the client.
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST) # status: Named Status codes  more explicit identifiers for each status code

